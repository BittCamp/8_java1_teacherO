package ex2;

public class Operator01 {
	public static void main(String[] args) {
		
		int a = 10;
		double b = 2.0;
		System.out.println("a + b = " + (a+b));
		System.out.println("a - b = " + (a-b));
		System.out.println("a * b = " + (a*b));
		System.out.println("a / b = " + (a/b));
		System.out.println("a % b = " + (a%b));
		// 인티저 더블 연산하면 더블이 더커서 형식이 더블에 맞춰진다.
		// 더 상위 형식을 따라간다.
  }

}