package ex2;

public class Operator05 {
	public static void main(String[] args) {
		int a = 10,b  = 10, c  = 10 , d = 10;
		
		System.out.println("a++ => " + (a++) ); // 출력하고 더함.
		System.out.println("a => " + a); 
		System.out.println("++b => " + (++b)); // 더하고 출력
		System.out.println("c-- => "+ (c--));  // 출력하고 뺌
		System.out.println("--d => " + (--d)); // 빼고 출력
		
		
	}
}
