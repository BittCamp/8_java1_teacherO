package ex1;



public class Datatype05 {
	public static void main(String[] args) {
		boolean t = true;
		boolean f = false;
		
		System.out.println(t);
		System.out.println(f);
		// == 같은지 안같은지 판별하게 하는 것.
		System.out.println(t==f);
		// != 같지 않은지 판별하는 것.
		System.out.println(t!=f); 
		System.out.println(true == false);
		System.out.println(true!=false);
	}
}
